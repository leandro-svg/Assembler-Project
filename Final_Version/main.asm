	CBI PORTD,4
.ENDMACRO

.MACRO SCREEN_ON
	CBI PORTB, 3
	SBRC @0, @1
	SBI PORTB, 3
	CBI PORTB,5
	SBI PORTB,5
.ENDMACRO

.MACRO SHOW_SCREEN
	LDI VAL4,low(@0)
	LDI VAL2,high(@0)
	ADD VAL4, VAL
	ADC VAL2, VAL7
	LDI R25,8
	IN R18, PINB
	BST R18,0
	BRTC SwitchLow
	SwitchHigh:	
		SBI PINC,2
	SwitchLow: NOP
	RETI
.ENDMACRO

.MACRO ITEM_SCREEN
	LDI ZH, high(CharacterTable<<1)
	LDI ZL, low(CharacterTable<<1)	
	LDI R25, 8
	ADD ZL,@0
	LDI VAL6 ,0
	ADC ZH, VAL6
	ADD ZL,@2
	LDI VAL6 ,0
	ADC ZH, VAL6

	MOV VAL6, VAL4
	MUL R25,VAL6
	ST X,R0
	LD VAL6, X
	ADD ZL, VAL6
	ST X,R1
	LD VAL6, X
	ADC ZH, VAL6

	MOV VAL6, VAL2
	MUL R25,VAL6
	ST X,R0
	LD VAL6, X
	ADD ZH, VAL6

	LPM R3, Z
	FUNC_SCREEN_ON R3
.ENDMACRO


.MACRO FUNC_SCREEN_ON
	SCREEN_ON @0, 0
	SCREEN_ON @0, 1
	SCREEN_ON @0, 2
	SCREEN_ON @0, 3
	SCREEN_ON @0, 4
.ENDMACRO

.MACRO FUNC_SCREEN_ON_R29
	SCREEN_ON @0, 6
	SCREEN_ON @0, 5
	SCREEN_ON @0, 4
	SCREEN_ON @0, 3
	SCREEN_ON @0, 2
	SCREEN_ON @0, 1
	SCREEN_ON @0, 0
.ENDMACRO

.MACRO CHANGE_TIMER
	SBI PINB,1
	STS TCNT1H,r21
	STS TCNT1L,r26
	RETI
.ENDMACRO

