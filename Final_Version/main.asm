.MACRO Change_register_column
	;INPUT CONFIGURATION
	CBI DDRD,7  
	SBI PORTD,7
	CBI DDRD,6 
	SBI PORTD,6
	CBI DDRD,5
	SBI PORTD,5
	CBI DDRD,4  
	SBI PORTD,4
	;OUTPUT CONFIGURATION
	SBI DDRD, 3 
	CBI PORTD,3
	SBI DDRD, 2
	CBI PORTD,2
	SBI DDRD, 1 
	CBI PORTD,1
	SBI DDRD, 0 
	CBI PORTD,0
.ENDMACRO

.MACRO Change_register_row
	;INPUT CONFIGURATION
	CBI DDRD,3  
	SBI PORTD,3
	CBI DDRD,2 
	SBI PORTD,2
	CBI DDRD,1  
	SBI PORTD,1
	CBI DDRD,0  
	SBI PORTD,0
